//structure of c++
#include<iostream>//header file
int main()//starting point of program execution
{
    std::cout<<"Hello world!" <<std::endl;//std-standard library,endl-end of line (alternative of \n)
    std::cout<<"enter two numbers";//cout-output statement(used instead of printf in c++)
}
                                        //   OR
//structure of c++
#include<iostream>//header file
using namespace std;//used to avoid using std
int main()//starting point of program execution
{
    cout<<"Hello world!" <<endl;//std-standard library,endl-end of line (alternative of \n)
    cout<<"enter two numbers";//cout-output statement(used instead of printf in c++)
}